<?php
	//AutoIncrement(++) Operators
	$a=100;
	$b=100; 
	echo "Value of a before Incrementation = $a";
	++$a;
	echo "<br>Value of a after Pre - Incrementation = $a";
	$b++;
	echo "<br>Value of a after Post - Incrementation = $b";
	
	//AutoDecrement(--) Operators
	$a=100;
	$b=100;
	echo "<br><br>Value of a before Decrementation = $a";
	--$a;
	echo "<br>Value of a after Pre - Decrementation = $a";
	$b--;
	echo "<br>Value of a after Post - Decrementation = $b";
?>